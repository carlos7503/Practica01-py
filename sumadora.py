numero1 = int(input("numero1: "))
numero2 = int(input("numero2: "))

def suma(a,b):
    return a + b
print(suma(numero1, numero2))

def resta(a,b):
    return a - b
print(resta(numero1, numero2))

def multipli(a,b):
    return a * b
print(multipli(numero1, numero2))

def division(a,b):
    return a /b
print(division(numero1, numero2))
